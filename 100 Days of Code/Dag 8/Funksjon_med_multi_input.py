
#Funksjon som tar imot to argumenter til to parameter
def hils_med_fornavn_og_etternavn(fornavn, etternavn):
    print(f"Heisann {fornavn}")
    print(f"Etternavnet ditt er visst: {etternavn}")
    print(f"Hele navnet ditt er dermed: {fornavn} {etternavn}")

#Tester funksjonen
hils_med_fornavn_og_etternavn("Marius", "Holtmoen")
