
#Det er mange ulike matematiske operatorer som kan benyttes i Python.
#Majoriteten skrives på samme måten som i klassisk matte

Tall_1 = 5
Tall_2 = 8

#Eks: - Addering

print(Tall_1 + Tall_2)

#Eks: - Substraksjon

print(Tall_1 - Tall_2)

#Eks: - Multiplikasjon

print(Tall_1 * Tall_2)

#Eks: - Divisjon

print(Tall_1 / Tall_2)

#Eks: - Eksponent

print(Tall_1 ** Tall_2)

#Prioriteringsrekkefølge ved utregning:
# 1. Paranteser ()
# 2. Potenser **
# 3. Multiplikasjon *
# 4. Divisjon /
# 5. Addisjon
# 6. Substraksjon
# NB! Husk at paranteser kan benyttes for å manipulere rekkefølgen for matematiske utregninger.